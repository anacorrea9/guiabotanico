// Função para renderizar todas as plantas ao carregar a página
function carregarGaleriaInicial() {
    renderizarPlantas(plantas); // Renderiza todas as plantas
}

// Chama a função quando a página termina de carregar
window.addEventListener('DOMContentLoaded', carregarGaleriaInicial);

function renderizarPlantas(plantasParaRenderizar) {
    const galeria = document.querySelector('.galeria-imagens');
    galeria.innerHTML = ''; // Limpa a galeria
    
    plantasParaRenderizar.forEach(planta => {
        const item = document.createElement('div');
        item.className = 'imagem-container';
        item.innerHTML = `
            <img src="${planta.imagem}" alt="${planta.nomeCientifico}" class="imagem-planta">
            <div class="legenda-planta">
                <p class="familia">${planta.familia}</p>
                <p class="nome-cientifico">${planta.nomeCientifico}</p>
                <p class="tipo-planta">${planta.tipo}</p>
            </div>
        `;
        galeria.appendChild(item);
    });
}